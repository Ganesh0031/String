public class Internalstring {
  public static void main(String[] args) {
    String s="Raghav";
    String m="Madhav";
    System.out.println(s);
    System.out.println(m);
    s="Madhav";
    m="Raghav";
    System.out.println();
    System.out.println(s);
    System.out.println(m);
  }
  
}
